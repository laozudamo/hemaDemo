<template>
  <div class="articleList">
    <van-pull-refresh
      v-model="isRefreshLoading"
      @refresh="onRefresh"
      success-text="刷新成功"
    >

    <van-list
      v-model="loading"
      :finished="finished"
      finished-text="没有更多了"
      @load="onLoad"
    >
      <van-cell v-for="(article,index) in articles" :key="index" :title="article.title" />
    </van-list>

    </van-pull-refresh>

  </div>
</template>

<script>
import { getRecomendArticles } from '@/api/articles.js'
export default {
  name: 'articleList', 
  components: {},
  props: {
    channel: {
      type: Object,
      require: true
    },
  },
  data () {
    return {
      articles: [],
      loading: false,
      finished: false,
      timestamp: null,
      isRefreshLoading: false,
    }
  },
  computed: {},
  watch: {},
  created () {},
  mounted () {},
  methods: {
    async onLoad() {
      /* 请求数据 */
      const { data } = await getRecomendArticles({
        channel_id: this.channel.id,
        timestamp: this.timestamp || Date.now(),
        with_top: 1
      })
      /* 把数据放到list里面 */
      const { results } = data.data
      this.articles = [...this.articles,...results]
      /* this.articles.push(...results) */
      this.loading = false
      console.log(data)

      /* 如果数据全部加载完 */
      if(results.length) {
        /* 更新加载 */
        this.timestamp = data.data.pre_timestamp
      } else {
        /* 加载结束 */
        this.finished = true
      }
    },

    async onRefresh() {
    const { data } = await getRecomendArticles({
      channel_id: this.channel.id,
      timestamp: Date.now(),
      with_top: 1
    })
    
    /* 往顶部追加数据 */
    const { results } = data.data
    this.articles.unshift(...results)
    this.isRefreshLoading = false
    }
  }
}
</script>

